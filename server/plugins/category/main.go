func GetCategory(text string) string {
	results := make(map[string]int)
	text = strings.ToLower(text)

	for category, list := range categories {
		results[category] = 0
		for _, word := range list {
			re := regexp.MustCompile(word)
			matches := re.FindAllString(text, -1)
			if matches != nil {
				results[category] += len(matches)
			}
		}
	}

	max := 0
	var maxStore string
	for category, count := range results {
		if count > max {
			max = count
			maxStore = category
		}
	}
	return maxStore
}
